package app.proyecto2.reque.muestreosapp.Modelo;

public class Operacion {
    int id;
    int nombre;

    public Operacion(int id, int nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public int getNombre() {
        return nombre;
    }

}
